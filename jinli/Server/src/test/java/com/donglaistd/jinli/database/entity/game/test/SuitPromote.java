package com.donglaistd.jinli.database.entity.game.test;
/*
* 提升效果加成
* */
public class SuitPromote {
    private double lifePromote;
    private double speedPromote;
    private double attackPromote;
    private double defencePromote;

    public double getLifePromote() {
        return lifePromote;
    }

    public void setLifePromote(double lifePromote) {
        this.lifePromote = lifePromote;
    }

    public double getSpeedPromote() {
        return speedPromote;
    }

    public void setSpeedPromote(double speedPromote) {
        this.speedPromote = speedPromote;
    }

    public double getAttackPromote() {
        return attackPromote;
    }

    public void setAttackPromote(double attackPromote) {
        this.attackPromote = attackPromote;
    }

    public double getDefencePromote() {
        return defencePromote;
    }

    public void setDefencePromote(double defencePromote) {
        this.defencePromote = defencePromote;
    }
}
